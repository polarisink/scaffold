    /**
     * 普通数据 查询缓存  null  缓存空值  需要限流不然 第一次 全部都是查询null
     *
     * @param keyPrefix  关键前缀
     * @param id         id
     * @param resultType 结果类型
     * @param time       时间
     * @param timeUnit   时间单位
     * @param dbFallback db回退
     * @return {@link R}
     */
    public static <R, ID> R queryCache(String keyPrefix, ID id, Class<R> resultType, Long time, TimeUnit timeUnit, Function<ID, R> dbFallback) {

        String key = keyPrefix + id;
        String json = get(key);
        // 是否命中缓存  空字符串 "" 也是false
        if (null != json && !json.isEmpty()) {
            log.info("命中缓存 缓存中存在数据");
            return JsonUtil.redisRead(json, resultType);
        }
        // 判断查询处理的数据 是 ""
        if (null != json) {
            log.info("缓存的是空字符串 ");
            return null;
        }
        log.info("缓存中 数据真的是不存在");
        // 查询数据库
        R r = dbFallback.apply(id);
        if (null == r) {
            // 查询出来的结果是null 缓存空值
            log.info("数据库也没有查询到数据");
            redisTemplate.opsForValue().set(key, "", 20, TimeUnit.SECONDS);
            return null;
        }
        // 查询出来的结果有值 就缓存起来
        set(key, r, time, timeUnit);
        // 返回数据库查询到的结果
        return r;

    }

